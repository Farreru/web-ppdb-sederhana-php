$baseURL = "http://localhost/ppdb/";
function callApi($class){
    global $baseURL;
    
    $url = $baseURL . "function/action.php?class=$class";
    $ch = curl_init();
    curl_setopt($ch, CURLOPT_URL, $url);
    curl_setopt($ch, CURLOPT_RETURNTRANSFER, true);
    $response = curl_exec($ch);
    curl_close($ch);

return $response;

}

